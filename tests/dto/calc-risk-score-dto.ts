export class CalcRiskScoreDto {
  income: number
  debt: number
  age: number
  employed: boolean
  loanAmount: number
  loanPeriod: number

  constructor(
    income: number,
    debt: number,
    age: number,
    employed: boolean,
    loanAmount: number,
    loanPeriod: number,
  ) {
    this.income = income
    this.debt = debt
    this.age = age
    this.employed = employed
    this.loanAmount = loanAmount
    this.loanPeriod = loanPeriod
  }

  static calculateLowRiscScore(): CalcRiskScoreDto {
    return new CalcRiskScoreDto(1000, 0, 25, true, 5000, 12)
  }
}
